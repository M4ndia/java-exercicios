public class PromocaoAutomatica {

	public static void main(String[] args) {

		byte a = 10;
		short b = 20;
		char c = 'A';
		int d = 30;
		long e = 40L;
		float f = 1.5f;
		double g = 2.5;

		int resultado1 = a + b;
		long resultado2 = e - d;
		double resultado3 = f * g;


		System.out.println("Resultado da soma (byte + short): " + resultado1);
		System.out.println("Resultado da subtração (Long - int): " + resultado2);
		System.out.println("Resultado da multiplicação (float * double): " + resultado3);
		System.out.println("Valor do char:" + c);


	}
 } 

 
		


